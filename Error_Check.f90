!==============================================================================!
  subroutine Error_Check(call_name, err)
!------------------------------------------------------------------------------!
  implicit none
!------------------------------------------------------------------------------!
  character(len=*) :: call_name
  integer          :: err
!------------------------------------------------------------------------------!

  if(err .ne. 0) then
    write (*,*)
    write (*, '(a,a,i2)')  call_name, " error: ", err
    write (*,*)
    stop
  end if

  end subroutine
